  function clone(obj) {
    return JSON.parse(JSON.stringify(obj));
  }

  function buildDownstreamFlow(name, handler, options) {
    var steps = [
      clone(INFRA.mqttRecv),
      clone(INFRA.mqttRoute),
      handler,
      clone(INFRA.cloudFlowIn),
      clone(INFRA.dispatchIn),
      clone(INFRA.reverseSend),
      clone(INFRA.wechatDown),
    ];
    return { name: name, direction: 'downstream', steps: steps };
  }

  function buildUpstreamFlow(name, convertHandler, options) {
    var steps = [
      clone(INFRA.wechatUp),
      clone(INFRA.ipcPoll),
      clone(INFRA.dispatchOut),
      clone(INFRA.msgHandle),
      clone(INFRA.businessRoute),
      clone(INFRA.cloudFlowOut),
      convertHandler,
      clone(INFRA.mqttPublish),
    ];
    return { name: name, direction: 'upstream', steps: steps };
  }

  function buildEventUpstreamFlow(name, convertHandler) {
    var steps = [
      clone(INFRA.wechatUp),
      clone(INFRA.ipcPoll),
      clone(INFRA.dispatchOut),
      convertHandler,
      clone(INFRA.mqttPublish),
    ];
    return { name: name, direction: 'upstream-event', steps: steps };
  }

